package com.pls.ltlrating.integration.ltllifecycle;

public class LtlLifecycleClientException extends RuntimeException {

    private static final long serialVersionUID = 6782768987410207494L;

    public LtlLifecycleClientException() {
        super();
    }

    public LtlLifecycleClientException(String message, Throwable cause) {
        super(message, cause);
    }

    public LtlLifecycleClientException(String message) {
        super(message);
    }

    public LtlLifecycleClientException(Throwable cause) {
        super(cause);
    }

    
}
